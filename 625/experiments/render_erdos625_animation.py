def parser() -> argparse.ArgumentParser:
    result = argparse.ArgumentParser(description="Render the audited finite animation for Erdos Problem 625.")
    result.add_argument("--output-dir", type=Path, default=Path(__file__).resolve().parents[1] / "assets" / "animations")
    result.add_argument("--n", type=int, default=N)
    result.add_argument("--seed", type=int, default=SEED)
    result.add_argument("--width", type=int, default=1280)
    result.add_argument("--height", type=int, default=720)
    result.add_argument("--fps", type=int, default=24)
    result.add_argument("--gif-fps", type=int, default=10)
    result.add_argument("--gif-width", type=int, default=960)
    result.add_argument("--duration", type=float, default=20.0)
    result.add_argument("--ffmpeg", help="explicit FFmpeg executable")
    return result


def main(argv: Sequence[str] | None = None) -> int:
    args = parser().parse_args(argv)
    if min(args.width, args.height, args.fps, args.gif_fps, args.gif_width) <= 0:
        raise ValueError("render dimensions and frame rates must be positive")
    if args.duration < 17:
        raise ValueError("duration must be at least 17 seconds for the full storyboard")
    render(args)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
