def clamp(value: float, low: float = 0.0, high: float = 1.0) -> float:
    return max(low, min(high, value))


def smoothstep(value: float) -> float:
    value = clamp(value)
    return value * value * (3.0 - 2.0 * value)


def rgb(color: str) -> tuple[int, int, int]:
    color = color.lstrip("#")
    return tuple(int(color[i : i + 2], 16) for i in (0, 2, 4))


def mix(a: str, b: str, amount: float) -> tuple[int, int, int]:
    amount = clamp(amount)
    ca, cb = rgb(a), rgb(b)
    return tuple(round(x + (y - x) * amount) for x, y in zip(ca, cb))


def mask(vertices: Iterable[int]) -> int:
    result = 0
    for vertex in vertices:
        result |= 1 << (vertex - 1)
    return result


def load_exact_module():
    path = Path(__file__).with_name("exact_chi_zeta.py")
    spec = importlib.util.spec_from_file_location("erdos625_exact", path)
    if spec is None or spec.loader is None:
        raise RuntimeError(f"cannot import exact solver from {path}")
    module = importlib.util.module_from_spec(spec)
    sys.modules[spec.name] = module
    spec.loader.exec_module(module)
    return module


def edge_list(adj: Sequence[int]) -> tuple[tuple[int, int], ...]:
    return tuple(
        (i + 1, j + 1)
        for i in range(len(adj))
        for j in range(i + 1, len(adj))
        if (adj[i] >> j) & 1
    )


def induced_edges(vertices: Sequence[int], edges: set[tuple[int, int]]) -> set[tuple[int, int]]:
    chosen = set(vertices)
    return {edge for edge in edges if edge[0] in chosen and edge[1] in chosen}


def require(condition: bool, message: str) -> None:
    if not condition:
        raise RuntimeError(f"animation validation failed: {message}")


def validate_instance(exact):
    adj = exact.random_graph(N, SEED)
    edges = edge_list(adj)
    edge_set = set(edges)
    digest = exact.graph_digest(adj)
    inv = exact.exact_invariants(adj)
    comp = exact.exact_invariants(exact.complement(adj))

    require(digest == EXPECTED_DIGEST, "unexpected graph digest")
    require(len(edges) == EXPECTED_EDGES, "unexpected edge count")
    require(
        (inv.alpha, inv.omega, inv.chi, inv.zeta) == (5, 6, 6, 3),
        "unexpected exact invariants",
    )
    require(comp.zeta == inv.zeta, "cochromatic number is not complement-invariant")

    chi_partition = tuple(exact.Column(mask(vertices), "I") for _, vertices, _ in CHI_CLASSES)
    zeta_partition = tuple(exact.Column(mask(vertices), kind) for _, kind, vertices, _ in ZETA_PARTS)
    exact.verify_partition(chi_partition, adj)
    exact.verify_partition(zeta_partition, adj)

    require(exact._is_clique(mask(K6_CERTIFICATE), adj), "K6 certificate is invalid")
    require(
        exact._is_independent(mask(INDEPENDENT_TRIPLE), adj),
        "independent-triple certificate is invalid",
    )
    two_k2_edges = induced_edges(INDUCED_2K2, edge_set)
    require(two_k2_edges == {(1, 3), (2, 6)}, "induced 2K2 certificate is invalid")

    return adj, edges, inv


def spring_layout(n: int, edges: Sequence[tuple[int, int]], iterations: int = 650):
    """Small deterministic Fruchterman--Reingold layout without NetworkX."""

    positions = []
    for i in range(n):
        angle = 2.0 * math.pi * ((5 * i) % n) / n - math.pi / 2.0
        radius = 0.82 + 0.08 * math.sin((i + 1) * 2.17)
        positions.append([radius * math.cos(angle), radius * math.sin(angle)])

    zero_edges = [(a - 1, b - 1) for a, b in edges]
    area = 4.0
    ideal = math.sqrt(area / n)
    for step in range(iterations):
        disp = [[0.0, 0.0] for _ in range(n)]
        for i in range(n):
            for j in range(i + 1, n):
                dx = positions[i][0] - positions[j][0]
                dy = positions[i][1] - positions[j][1]
                dist = max(math.hypot(dx, dy), 1e-4)
                force = ideal * ideal / dist
                ux, uy = dx / dist, dy / dist
                disp[i][0] += ux * force
                disp[i][1] += uy * force
                disp[j][0] -= ux * force
                disp[j][1] -= uy * force
        for i, j in zero_edges:
            dx = positions[i][0] - positions[j][0]
            dy = positions[i][1] - positions[j][1]
            dist = max(math.hypot(dx, dy), 1e-4)
            force = 0.72 * dist * dist / ideal
            ux, uy = dx / dist, dy / dist
            disp[i][0] -= ux * force
            disp[i][1] -= uy * force
            disp[j][0] += ux * force
            disp[j][1] += uy * force
        temperature = 0.14 * (1.0 - step / iterations) + 0.004
        for i in range(n):
            length = max(math.hypot(*disp[i]), 1e-9)
            move = min(length, temperature)
            positions[i][0] += disp[i][0] / length * move
            positions[i][1] += disp[i][1] / length * move
        mean_x = sum(p[0] for p in positions) / n
        mean_y = sum(p[1] for p in positions) / n
        for p in positions:
            p[0] -= mean_x
            p[1] -= mean_y

    max_abs = max(max(abs(p[0]), abs(p[1])) for p in positions)
    return tuple((p[0] / max_abs, p[1] / max_abs) for p in positions)


def font_path(*names: str) -> str | None:
    windows_fonts = Path(os.environ.get("WINDIR", "C:/Windows")) / "Fonts"
    for name in names:
        candidate = windows_fonts / name
        if candidate.exists():
            return str(candidate)
    return None


class Fonts:
    def __init__(self, height: int):
        scale = height / 720.0
        regular = font_path("segoeui.ttf", "arial.ttf", "calibri.ttf")
        bold = font_path("seguisb.ttf", "arialbd.ttf", "calibrib.ttf") or regular
        self.sources = {
            "regular": Path(regular).name if regular else "Pillow default",
            "bold": Path(bold).name if bold else "Pillow default",
        }

        def load(path: str | None, size: int):
            if path:
                return ImageFont.truetype(path, max(10, round(size * scale)))
            return ImageFont.load_default()

        self.title = load(bold, 38)
        self.subtitle = load(regular, 23)
        self.section = load(bold, 27)
        self.body = load(regular, 21)
        self.body_bold = load(bold, 21)
        self.small = load(regular, 17)
        self.small_bold = load(bold, 17)
        self.node = load(bold, 21)
        self.big = load(bold, 46)


def pixel_positions(layout, center: tuple[float, float], scale: float):
    cx, cy = center
    return tuple((cx + scale * x, cy - scale * y) for x, y in layout)


def dashed_ellipse(draw: ImageDraw.ImageDraw, box, fill, width: int, dash: int = 17, gap: int = 11):
    angle = 0
    while angle < 360:
        draw.arc(box, angle, min(angle + dash, 360), fill=fill, width=width)
        angle += dash + gap


def draw_centered(draw, xy, text: str, font, fill, anchor: str = "mm"):
    draw.text(xy, text, font=font, fill=fill, anchor=anchor)


def draw_header(image: Image.Image, fonts: Fonts, title: str, subtitle: str):
    draw = ImageDraw.Draw(image)
    draw.text((54, 28), title, font=fonts.title, fill=NAVY)
    draw.text((56, 78), subtitle, font=fonts.subtitle, fill=MUTED)
    draw.line((54, 112, image.width - 54, 112), fill=rgb(GOLD), width=3)


def draw_footer(image: Image.Image, fonts: Fonts):
    draw = ImageDraw.Draw(image)
    text = "Selected illustrative seed 78 • exact finite example • not representative or theorem evidence"
    draw_centered(draw, (image.width / 2, image.height - 22), text, fonts.small, MUTED)


def draw_panel(image: Image.Image, box, title: str, subtitle: str, fonts: Fonts):
    draw = ImageDraw.Draw(image, "RGBA")
    draw.rounded_rectangle(box, radius=20, fill=rgb(PAPER) + (244,), outline=rgb("#C9D1DE") + (255,), width=2)
    x0, y0, x1, _ = box
    draw.text((x0 + 24, y0 + 20), title, font=fonts.section, fill=NAVY)
    draw.text((x0 + 24, y0 + 59), subtitle, font=fonts.small, fill=MUTED)
    draw.line((x0 + 24, y0 + 88, x1 - 24, y0 + 88), fill=rgb("#D5DBE5") + (255,), width=2)


def draw_row(image: Image.Image, fonts: Fonts, x: int, y: int, color: str, label: str, text: str,
             opacity: float = 1.0, kind: str | None = None):
    opacity = clamp(opacity)
    draw = ImageDraw.Draw(image, "RGBA")
    alpha = round(255 * opacity)
    fill = rgb(color) + (alpha,)
    draw.ellipse((x, y + 5, x + 25, y + 30), fill=fill, outline=rgb(INK) + (round(170 * opacity),), width=2)
    if kind == "I":
        dashed_ellipse(draw, (x - 4, y + 1, x + 29, y + 34), fill=rgb(color) + (alpha,), width=2)
    elif kind == "C":
        draw.ellipse((x - 4, y + 1, x + 29, y + 34), outline=fill, width=3)
    draw.text((x + 38, y + 4), label, font=fonts.body_bold, fill=rgb(INK) + (alpha,))
    draw.text((x + 91, y + 4), text, font=fonts.body, fill=rgb(INK) + (alpha,))


def draw_graph(
    image: Image.Image,
    positions,
    edges: Sequence[tuple[int, int]],
    fonts: Fonts,
    *,
    node_fills: dict[int, tuple[int, int, int]] | None = None,
    node_kinds: dict[int, str] | None = None,
    edge_alpha: int = 120,
    edge_width: int = 2,
    special_edges: dict[tuple[int, int], tuple[str, int, int]] | None = None,
    special_nodes: dict[int, str] | None = None,
    radius: int = 24,
):
    node_fills = node_fills or {}
    node_kinds = node_kinds or {}
    special_edges = special_edges or {}
    special_nodes = special_nodes or {}
    draw = ImageDraw.Draw(image, "RGBA")

    for a, b in edges:
        key = (min(a, b), max(a, b))
        color, width, alpha = special_edges.get(key, (EDGE, edge_width, edge_alpha))
        draw.line((*positions[a - 1], *positions[b - 1]), fill=rgb(color) + (alpha,), width=width)

    for vertex, (x, y) in enumerate(positions, start=1):
        fill = node_fills.get(vertex, rgb(NEUTRAL_NODE))
        outline = special_nodes.get(vertex, INK)
        box = (x - radius, y - radius, x + radius, y + radius)
        kind = node_kinds.get(vertex)
        if kind == "I":
            dashed_ellipse(draw, (x - radius - 6, y - radius - 6, x + radius + 6, y + radius + 6),
                           fill=rgb(outline) + (255,), width=3)
        elif kind == "C":
            draw.ellipse((x - radius - 6, y - radius - 6, x + radius + 6, y + radius + 6),
                         outline=rgb(outline) + (255,), width=4)
        draw.ellipse(box, fill=fill + (255,), outline=rgb(outline) + (255,), width=3)
        luminance = 0.2126 * fill[0] + 0.7152 * fill[1] + 0.0722 * fill[2]
        label_color = WHITE if luminance < 145 else INK
        draw_centered(draw, (x, y - 1), str(vertex), fonts.node, label_color)


def class_fill_map(classes, progress: float):
    fills: dict[int, tuple[int, int, int]] = {}
    outlines: dict[int, str] = {}
    for index, item in enumerate(classes):
        if len(item) == 3:
            _, vertices, color = item
        else:
            _, _, vertices, color = item
        amount = smoothstep(progress - index)
        for vertex in vertices:
            fills[vertex] = mix(NEUTRAL_NODE, color, amount)
            if amount > 0.02:
                outlines[vertex] = color
    return fills, outlines


def exact_edges_within(vertices: Sequence[int], edges: set[tuple[int, int]]):
    chosen = set(vertices)
    return {edge for edge in edges if edge[0] in chosen and edge[1] in chosen}


def render_frame(t: float, width: int, height: int, fonts: Fonts, layout, edges):
    image = Image.new("RGB", (width, height), BG)
    edge_set = set(edges)
    scale_x = width / 1280.0
    scale_y = height / 720.0
    single_center = (385 * scale_x, 390 * scale_y)
    single_scale = 270 * min(scale_x, scale_y)
    positions = pixel_positions(layout, single_center, single_scale)
    radius = max(15, round(24 * min(scale_x, scale_y)))

    if t < 1.5:
        draw_header(image, fonts, "Erdős Problem 625", "One selected seeded instance modeled on G(12, 1/2)")
        intro = smoothstep(t / 1.1)
        fills = {v: mix(BG, NEUTRAL_NODE, intro) for v in range(1, N + 1)}
        draw_graph(image, positions, (), fonts, node_fills=fills, edge_alpha=0, radius=radius)
        draw_panel(image, (770 * scale_x, 154 * scale_y, 1228 * scale_x, 570 * scale_y),
                   "A fixed seeded graph", "Twelve labelled vertices; one seeded bit per pair.", fonts)
        draw = ImageDraw.Draw(image)
        draw.text((802 * scale_x, 286 * scale_y), "n = 12", font=fonts.big, fill=NAVY)
        draw.text((802 * scale_x, 355 * scale_y), "p = 1/2", font=fonts.big, fill=NAVY)
        draw.text((802 * scale_x, 435 * scale_y), "seed = 78", font=fonts.section, fill=GOLD)

    elif t < 5.4:
        progress = smoothstep((t - 1.5) / 3.9)
        draw_header(image, fonts, "Building the seeded instance", "All 66 possible pairs are tested; 40 edges remain.")
        draw = ImageDraw.Draw(image, "RGBA")
        all_pairs = [(a, b) for a in range(1, N + 1) for b in range(a + 1, N + 1)]
        faint = round(22 * (1.0 - progress))
        for a, b in all_pairs:
            draw.line((*positions[a - 1], *positions[b - 1]), fill=rgb(LIGHT_EDGE) + (faint,), width=1)
        reveal = progress * len(edges)
        visible_edges = []
        special = {}
        for index, edge in enumerate(edges):
            amount = smoothstep(reveal - index)
            if amount > 0:
                visible_edges.append(edge)
                special[edge] = (NAVY, 3, round(190 * amount))
        draw_graph(image, positions, visible_edges, fonts, edge_alpha=0, special_edges=special, radius=radius)
        draw_panel(image, (770 * scale_x, 154 * scale_y, 1228 * scale_x, 570 * scale_y),
                   "Seeded pair decisions", "The graph is fixed after the last revealed edge.", fonts)
        draw2 = ImageDraw.Draw(image)
        count = min(len(edges), math.floor(reveal))
        draw2.text((802 * scale_x, 282 * scale_y), f"{count:02d} / 40", font=fonts.big, fill=NAVY)
        draw2.text((804 * scale_x, 346 * scale_y), "present edges", font=fonts.body, fill=MUTED)
        bar = (804 * scale_x, 410 * scale_y, 1178 * scale_x, 437 * scale_y)
        draw2.rounded_rectangle(bar, radius=12, fill=rgb("#DCE2EB"))
        draw2.rounded_rectangle((bar[0], bar[1], bar[0] + (bar[2] - bar[0]) * progress, bar[3]),
                                radius=12, fill=rgb(GOLD))

    elif t < 7.0:
        draw_header(image, fonts, "The selected graph", "The layout and edge set now stay fixed.")
        draw_graph(image, positions, edges, fonts, edge_alpha=130, radius=radius)
        draw_panel(image, (770 * scale_x, 154 * scale_y, 1228 * scale_x, 570 * scale_y),
                   "Exact finite instance", "Solved by exact minimum set-cover branch-and-bound.", fonts)
        draw = ImageDraw.Draw(image)
        draw.text((802 * scale_x, 258 * scale_y), "12 vertices", font=fonts.section, fill=NAVY)
        draw.text((802 * scale_x, 316 * scale_y), "40 edges", font=fonts.section, fill=NAVY)
        draw.text((802 * scale_x, 398 * scale_y), "χ(G) = 6", font=fonts.big, fill=GOLD)
        draw.text((802 * scale_x, 470 * scale_y), "ζ(G) = 3", font=fonts.big, fill=GREEN)

    elif t < 11.6:
        draw_header(image, fonts, "Proper coloring", "Each color class must be an independent set.")
        if t < 8.25:
            k6_edges = exact_edges_within(K6_CERTIFICATE, edge_set)
            special_edges = {edge: (GOLD, 6, 245) for edge in k6_edges}
            special_nodes = {v: GOLD for v in K6_CERTIFICATE}
            draw_graph(image, positions, edges, fonts, edge_alpha=45,
                       special_edges=special_edges, special_nodes=special_nodes, radius=radius)
            draw_panel(image, (770 * scale_x, 154 * scale_y, 1228 * scale_x, 570 * scale_y),
                       "Why at least six colors?", "The highlighted vertices induce a complete graph K₆.", fonts)
            draw = ImageDraw.Draw(image)
            draw.text((802 * scale_x, 275 * scale_y), "K₆ = {2, 5, 6, 8, 10, 12}", font=fonts.body_bold, fill=GOLD)
            draw.text((802 * scale_x, 347 * scale_y), "Every pair is adjacent.", font=fonts.body, fill=INK)
            draw.text((802 * scale_x, 405 * scale_y), "So χ(G) ≥ 6", font=fonts.big, fill=NAVY)
        else:
            progress = clamp((t - 8.25) / 2.55 * len(CHI_CLASSES), 0, len(CHI_CLASSES))
            fills, outlines = class_fill_map(CHI_CLASSES, progress)
            draw_graph(image, positions, edges, fonts, node_fills=fills, edge_alpha=54,
                       special_nodes=outlines, radius=radius)
            draw_panel(image, (770 * scale_x, 136 * scale_y, 1228 * scale_x, 606 * scale_y),
                       "Six independent color classes", "No edge has equal-colored endpoints.", fonts)
            for index, (label, vertices, color) in enumerate(CHI_CLASSES):
                opacity = smoothstep(progress - index)
                vertex_text = "{" + ", ".join(map(str, vertices)) + "}"
                draw_row(image, fonts, round(802 * scale_x), round((235 + index * 53) * scale_y),
                         color, label, vertex_text, opacity=opacity, kind="I")
            if progress >= len(CHI_CLASSES) - 0.02:
                draw = ImageDraw.Draw(image)
                draw.text((842 * scale_x, 555 * scale_y), "K₆ lower bound + 6-color witness gives χ(G) = 6",
                          font=fonts.small_bold, fill=NAVY)

    elif t < 16.6:
        draw_header(image, fonts, "Cochromatic partition", "Each part may induce either a clique or an independent set.")
        progress = clamp((t - 11.6) / 2.7 * len(ZETA_PARTS), 0, len(ZETA_PARTS))
        fills, outlines = class_fill_map(ZETA_PARTS, progress)
        kinds: dict[int, str] = {}
        special_edges = {}
        for index, (_, kind, vertices, color) in enumerate(ZETA_PARTS):
            amount = smoothstep(progress - index)
            if amount > 0.02:
                for vertex in vertices:
                    kinds[vertex] = kind
                if kind == "C":
                    for edge in exact_edges_within(vertices, edge_set):
                        special_edges[edge] = (color, 6, round(235 * amount))
        draw_graph(image, positions, edges, fonts, node_fills=fills, node_kinds=kinds,
                   edge_alpha=35, special_edges=special_edges, special_nodes=outlines, radius=radius)
        draw_panel(image, (770 * scale_x, 132 * scale_y, 1228 * scale_x, 614 * scale_y),
                   "Three homogeneous parts", "Solid ring = clique; dashed ring = independent set.", fonts)
        for index, (label, kind, vertices, color) in enumerate(ZETA_PARTS):
            opacity = smoothstep(progress - index)
            vertex_text = "{" + ", ".join(map(str, vertices)) + "}"
            draw_row(image, fonts, round(802 * scale_x), round((230 + index * 60) * scale_y),
                     color, label, vertex_text, opacity=opacity, kind=kind)

        witness_progress = clamp((t - 14.45) / 1.65 * 3.0, 0, 3)
        witness_rows = (
            ("K₆", "rules out two independent parts"),
            ("I₃ {1,2,4}", "rules out two clique parts"),
            ("2K₂ {1,2,3,6}", "rules out clique + independent"),
        )
        draw = ImageDraw.Draw(image, "RGBA")
        for index, (label, explanation) in enumerate(witness_rows):
            opacity = smoothstep(witness_progress - index)
            alpha = round(255 * opacity)
            y = (440 + index * 46) * scale_y
            draw.line(
                ((804 * scale_x, y + 13 * scale_y), (811 * scale_x, y + 21 * scale_y),
                 (825 * scale_x, y + 4 * scale_y)),
                fill=rgb(GREEN) + (alpha,),
                width=max(2, round(3 * min(scale_x, scale_y))),
            )
            draw.text((833 * scale_x, y), label, font=fonts.small_bold, fill=rgb(INK) + (alpha,))
            draw.text((953 * scale_x, y), explanation, font=fonts.small, fill=rgb(MUTED) + (alpha,))
        if witness_progress >= 2.95:
            draw.text((835 * scale_x, 582 * scale_y), "3-part witness + no 2-part option gives ζ(G) = 3",
                      font=fonts.small_bold, fill=NAVY)

    else:
        draw_header(image, fonts, "The same graph, two exact partitions", "Only the vertex partition changes.")
        left_pos = pixel_positions(layout, (320 * scale_x, 400 * scale_y), 205 * min(scale_x, scale_y))
        right_pos = pixel_positions(layout, (950 * scale_x, 400 * scale_y), 205 * min(scale_x, scale_y))
        chi_fills, chi_outlines = class_fill_map(CHI_CLASSES, len(CHI_CLASSES))
        draw_graph(image, left_pos, edges, fonts, node_fills=chi_fills, edge_alpha=42,
                   special_nodes=chi_outlines, radius=max(13, round(radius * 0.78)))

        zeta_fills, zeta_outlines = class_fill_map(ZETA_PARTS, len(ZETA_PARTS))
        zeta_kinds = {vertex: kind for _, kind, vertices, _ in ZETA_PARTS for vertex in vertices}
        zeta_special = {}
        for _, kind, vertices, color in ZETA_PARTS:
            if kind == "C":
                for edge in exact_edges_within(vertices, edge_set):
                    zeta_special[edge] = (color, 5, 235)
        draw_graph(image, right_pos, edges, fonts, node_fills=zeta_fills, node_kinds=zeta_kinds,
                   edge_alpha=30, special_edges=zeta_special, special_nodes=zeta_outlines,
                   radius=max(13, round(radius * 0.78)))

        draw = ImageDraw.Draw(image)
        draw_centered(draw, (320 * scale_x, 145 * scale_y), "Proper coloring", fonts.section, NAVY)
        draw_centered(draw, (320 * scale_x, 184 * scale_y), "χ(G) = 6 independent classes", fonts.body, MUTED)
